// FUNCIÓN CORREGIDA: HP POR HILERA
// ======================================================

function calcularHpPorHileraCadena(potenciaDiseno) {
    const numeroHileras = Number(obtenerValorTexto("numeroHilerasCadena"));

    let datosHilera = null;

    if (
        typeof tablaFactorHilerasCadena !== "undefined" &&
        tablaFactorHilerasCadena?.[numeroHileras]
    ) {
        datosHilera = tablaFactorHilerasCadena[numeroHileras];
    } else {
        datosHilera = FACTORES_HILERAS_COMPATIBLES_CADENA[numeroHileras];
    }

    if (!datosHilera) {
        colocarTexto("hpPorHileraCadena", "-");

        const inputFactorHileras = document.getElementById("factorHilerasCadena");
        if (inputFactorHileras) inputFactorHileras.value = "";

        colocarTexto(
            "detalleHpPorHileraCadena",
            "Seleccione el número de hileras."
        );

        return NaN;
    }

    const factorHilera = Number(datosHilera.factor);

    const inputFactorHileras = document.getElementById("factorHilerasCadena");
    if (inputFactorHileras) {
        inputFactorHileras.value = `${formatearNumero(factorHilera, 2)} - ${datosHilera.descripcion}`;
    }

    if (!Number.isFinite(potenciaDiseno) || potenciaDiseno <= 0) {
        colocarTexto("hpPorHileraCadena", "-");

        colocarTexto(
            "detalleHpPorHileraCadena",
            "Ingrese potencia y factor de seguridad válidos."
        );

        return NaN;
    }

    const hpPorHilera = potenciaDiseno / factorHilera;

    colocarTexto(
        "hpPorHileraCadena",
        `${formatearNumero(hpPorHilera, 3)} HP`
    );

    colocarTexto(
        "detalleHpPorHileraCadena",
        `HP por hilera = ${formatearNumero(potenciaDiseno, 3)} / ${formatearNumero(factorHilera, 2)} = ${formatearNumero(hpPorHilera, 3)} HP | Número de hileras: ${numeroHileras}`
    );

    return hpPorHilera;
}


