function construirReferenciaDimensionCadena(cadena, dientes, numeroHileras) {
    const cadenaTexto = String(cadena);
    const dientesTexto = String(dientes);
    const hileras = Number(numeroHileras);

    if (hileras === 1) {
        return `${cadenaTexto}B${dientesTexto}`;
    }

    if (hileras === 2) {
        return `${cadenaTexto}-2B${dientesTexto}`;
    }

    if (hileras >= 3) {
        return `${cadenaTexto}-3B${dientesTexto}`;
    }

    return `${cadenaTexto}B${dientesTexto}`;
}


function limpiarReferenciaCatalogo(ref) {
    return String(ref || "")
        .replace(/\s+/g, "")
        .replace(/R$/i, "")
        .toUpperCase();
}


function buscarDimensionesPinonCadena(cadena, dientes, numeroHileras) {
    const referenciaBase = construirReferenciaDimensionCadena(
        cadena,
        dientes,
        numeroHileras
    );

    const referenciaLimpia = limpiarReferenciaCatalogo(referenciaBase);

    if (
        typeof tablasDimensionesPinonCadena === "undefined" ||
        !Array.isArray(tablasDimensionesPinonCadena)
    ) {
        return {
            ok: false,
            referenciaBuscada: referenciaBase,
            mensaje: "No existe la tabla de dimensiones en el script."
        };
    }

    for (const tabla of tablasDimensionesPinonCadena) {
        if (String(tabla.cadena) !== String(cadena)) continue;

        if (!Array.isArray(tabla.filas)) continue;

        for (const fila of tabla.filas) {
            const referenciaFila = limpiarReferenciaCatalogo(fila.ref);

            if (referenciaFila === referenciaLimpia) {
                return {
                    ok: true,
                    referenciaBuscada: referenciaBase,
                    cadena: String(cadena),
                    dientes: Number(dientes),
                    numeroHileras: Number(numeroHileras),
                    dimension: {
                        ref: fila.ref,
                        D: normalizarNumeroCatalogo(fila.D),
                        Dm: fila.Dm,
                        L: fila.L,
                        d: fila.d
                    }
                };
            }
        }
    }

    return {
        ok: false,
        referenciaBuscada: referenciaBase,
        cadena: String(cadena),
        dientes: Number(dientes),
        numeroHileras: Number(numeroHileras),
        mensaje: "No se encontró referencia dimensional para esta combinación."
    };
}


