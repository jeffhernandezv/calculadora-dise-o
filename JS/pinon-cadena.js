// INTERPOLACIÓN DE CAPACIDAD POR RPM
// ======================================================

function interpolarCapacidadPorRPM(rpmTabla, hpTabla, rpmObjetivo) {
    if (
        !Array.isArray(rpmTabla) ||
        !Array.isArray(hpTabla) ||
        rpmTabla.length !== hpTabla.length ||
        !Number.isFinite(rpmObjetivo)
    ) {
        return NaN;
    }

    if (rpmObjetivo < rpmTabla[0] || rpmObjetivo > rpmTabla[rpmTabla.length - 1]) {
        return NaN;
    }

    for (let i = 0; i < rpmTabla.length; i++) {
        if (rpmObjetivo === rpmTabla[i]) {
            return Number(hpTabla[i]);
        }
    }

    for (let i = 0; i < rpmTabla.length - 1; i++) {
        const x1 = rpmTabla[i];
        const x2 = rpmTabla[i + 1];
        const y1 = Number(hpTabla[i]);
        const y2 = Number(hpTabla[i + 1]);

        if (rpmObjetivo >= x1 && rpmObjetivo <= x2) {
            if (!Number.isFinite(y1) || !Number.isFinite(y2)) return NaN;
            if (y1 <= 0 || y2 <= 0) return NaN;

            return y1 + ((rpmObjetivo - x1) * (y2 - y1)) / (x2 - x1);
        }
    }

    return NaN;
}


