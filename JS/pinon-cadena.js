// ======================================================
// FUNCIÓN PARA CREAR TABLAS DE DIMENSIONES DESDE CSV
// ======================================================

function crearTablaDimensionesDesdeCSV(cadena, hilerasPermitidas, textoCSV) {
    const lineas = textoCSV
        .trim()
        .split("\n")
        .map(linea => linea.trim())
        .filter(linea => linea.length > 0);

    if (lineas.length < 2) {
        return {
            cadena: String(cadena),
            hilerasPermitidas: hilerasPermitidas,
            filas: []
        };
    }

    const encabezados = dividirLineaCSV(lineas[0]);

    const filas = [];

    for (let i = 1; i < lineas.length; i++) {
        const valores = dividirLineaCSV(lineas[i]);
        const filaBase = {};

        encabezados.forEach((encabezado, indice) => {
            filaBase[encabezado] = valores[indice] || "";
        });

        // Hilera sencilla
        if (filaBase.REF_S) {
            filas.push({
                ref: filaBase.REF_S,
                D: filaBase.D,
                Dm: filaBase.Dm_S,
                L: filaBase.L_S,
                d: filaBase.d_S,
                tipo: "sencillo",
                hileras: 1
            });
        }

        // Hilera doble
        if (filaBase.REF_D) {
            filas.push({
                ref: filaBase.REF_D,
                D: filaBase.D,
                Dm: filaBase.Dm_D,
                L: filaBase.L_D,
                d: filaBase.d_D,
                tipo: "doble",
                hileras: 2
            });
        }

        // Hilera triple
        if (filaBase.REF_T) {
            filas.push({
                ref: filaBase.REF_T,
                D: filaBase.D,
                Dm: filaBase.Dm_T,
                L: filaBase.L_T,
                d: filaBase.d_T,
                tipo: "triple",
                hileras: 3
            });
        }
    }

    return {
        cadena: String(cadena),
        hilerasPermitidas: hilerasPermitidas,
        filas: filas
    };
}


// ======================================================
// DIVIDIR LÍNEAS CSV RESPETANDO COMILLAS
// Ejemplo: "21,2" no debe separarse como dos columnas
// ======================================================

function dividirLineaCSV(linea) {
    const resultado = [];
    let valorActual = "";
    let dentroDeComillas = false;

    for (let i = 0; i < linea.length; i++) {
        const caracter = linea[i];

        if (caracter === '"') {
            dentroDeComillas = !dentroDeComillas;
            continue;
        }

        if (caracter === "," && !dentroDeComillas) {
            resultado.push(valorActual.trim());
            valorActual = "";
            continue;
        }

        valorActual += caracter;
    }

    resultado.push(valorActual.trim());

    return resultado;
}

