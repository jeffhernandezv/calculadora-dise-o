// PASO 8 - LONGITUD DE CADENA Y NÚMERO DE ESLABONES
// L = 2C + 1,57(D + d) + (D - d)^2 / 4C
// N.E = L / P
// ======================================================

const pasosCadenaMm = {
    "25": 6.35,
    "35": 9.525,
    "40": 12.7,
    "41": 12.7,
    "50": 15.875,
    "60": 19.05,
    "80": 25.4,
    "100": 31.75,
    "120": 38.1,
    "140": 44.45,
    "160": 50.8,
    "200": 63.5,
    "240": 76.2
};


function obtenerPasoCadenaMm(numeroCadena) {
    const clave = String(numeroCadena);

    if (Object.prototype.hasOwnProperty.call(pasosCadenaMm, clave)) {
        return pasosCadenaMm[clave];
    }

    return NaN;
}


function redondearEslabonesParMasCercano(numeroEslabones) {
    if (!Number.isFinite(numeroEslabones)) {
        return NaN;
    }

    let eslabonesRedondeados = Math.round(numeroEslabones);

    if (eslabonesRedondeados % 2 !== 0) {
        const parInferior = eslabonesRedondeados - 1;
        const parSuperior = eslabonesRedondeados + 1;

        const diferenciaInferior = Math.abs(numeroEslabones - parInferior);
        const diferenciaSuperior = Math.abs(numeroEslabones - parSuperior);

        eslabonesRedondeados =
            diferenciaSuperior < diferenciaInferior
                ? parSuperior
                : parInferior;
    }

    if (eslabonesRedondeados < 2) {
        eslabonesRedondeados = 2;
    }

    return eslabonesRedondeados;
}


function limpiarResultadoLongitudCadena(mensaje) {
    colocarTexto("longitudCadenaCalculada", "Seleccione un piñón conductor");
    colocarTexto("pasoCadenaCalculado", "-");
    colocarTexto("numeroEslabonesTeorico", "-");
    colocarTexto("numeroEslabonesRecomendado", "-");
    colocarTexto("longitudCadenaAjustada", "-");

    colocarTexto(
        "detalleLongitudCadena",
        mensaje || "Al seleccionar el piñón conductor, aquí se calculará la longitud teórica de la cadena."
    );

    colocarTexto(
        "detalleNumeroEslabones",
        "El número de eslabones se redondea al número par más cercano."
    );
}


function calcularLongitudCadenaYEslabones(
    resultadoConductor,
    resultadoConducido,
    numeroCadena
) {
    if (
        !resultadoConductor ||
        !resultadoConducido ||
        !resultadoConductor.ok ||
        !resultadoConducido.ok
    ) {
        limpiarResultadoLongitudCadena(
            "No fue posible calcular la longitud de cadena porque faltan dimensiones del piñón conductor o del piñón conducido."
        );
        return;
    }

    const d = normalizarNumeroCatalogo(resultadoConductor.dimension.D);
    const D = normalizarNumeroCatalogo(resultadoConducido.dimension.D);

    if (
        !Number.isFinite(d) ||
        !Number.isFinite(D) ||
        d <= 0 ||
        D <= 0
    ) {
        limpiarResultadoLongitudCadena(
            "Los diámetros exteriores del conductor o del conducido no son válidos."
        );
        return;
    }

    const C = (D + 3 * d) / 2;

    if (!Number.isFinite(C) || C <= 0) {
        limpiarResultadoLongitudCadena(
            "La distancia entre centros no es válida para calcular la longitud de cadena."
        );
        return;
    }

    const paso = obtenerPasoCadenaMm(numeroCadena);

    if (!Number.isFinite(paso) || paso <= 0) {
        limpiarResultadoLongitudCadena(
            "No fue posible identificar el paso de la cadena seleccionada."
        );
        return;
    }

    const longitudCadena =
        2 * C +
        1.57 * (D + d) +
        Math.pow(D - d, 2) / (4 * C);

    const numeroEslabonesTeorico = longitudCadena / paso;
    const numeroEslabonesRecomendado =
        redondearEslabonesParMasCercano(numeroEslabonesTeorico);

    const longitudCadenaAjustada = numeroEslabonesRecomendado * paso;

    colocarTexto(
        "longitudCadenaCalculada",
        `${formatearNumero(longitudCadena, 2)} mm`
    );

    colocarTexto(
        "detalleLongitudCadena",
        `L = 2(${formatearNumero(C, 2)}) + 1,57(${formatearNumero(D, 2)} + ${formatearNumero(d, 2)}) + (${formatearNumero(D, 2)} - ${formatearNumero(d, 2)})² / 4(${formatearNumero(C, 2)}) = ${formatearNumero(longitudCadena, 2)} mm`
    );

    colocarTexto(
        "pasoCadenaCalculado",
        `${formatearNumero(paso, 3)} mm`
    );

    colocarTexto(
        "numeroEslabonesTeorico",
        `${formatearNumero(numeroEslabonesTeorico, 2)} eslabones`
    );

    colocarTexto(
        "numeroEslabonesRecomendado",
        `${numeroEslabonesRecomendado} eslabones`
    );

    colocarTexto(
        "detalleNumeroEslabones",
        `N.E = L / P = ${formatearNumero(longitudCadena, 2)} / ${formatearNumero(paso, 3)} = ${formatearNumero(numeroEslabonesTeorico, 2)} ≈ ${numeroEslabonesRecomendado} eslabones`
    );

    colocarTexto(
        "longitudCadenaAjustada",
        `${formatearNumero(longitudCadenaAjustada, 2)} mm`
    );
}
