function validarDatosMinimosCadena() {
    const potenciaMotor = obtenerValorNumerico("potenciaMotorCadena");
    const rpmConductor = obtenerValorNumerico("rpmConductorCadena");
    const rpmConducido = obtenerValorNumerico("rpmConducidoCadena");
    const numeroHileras = obtenerValorTexto("numeroHilerasCadena");
    const tipoCarga = obtenerValorTexto("tipoCargaCadena");
    const fuentePotencia = obtenerValorTexto("fuentePotenciaCadena");

    const errores = [];

    if (!Number.isFinite(potenciaMotor) || potenciaMotor <= 0) {
        errores.push("Ingrese una potencia de motor válida.");
    }

    if (!Number.isFinite(rpmConductor) || rpmConductor <= 0) {
        errores.push("Ingrese una RPM válida para el piñón conductor.");
    }

    if (!Number.isFinite(rpmConducido) || rpmConducido <= 0) {
        errores.push("Ingrese una RPM válida para el piñón conducido.");
    }

    if (!numeroHileras) {
        errores.push("Seleccione el número de hileras.");
    }

    if (!tipoCarga) {
        errores.push("Seleccione el tipo de carga.");
    }

    if (!fuentePotencia) {
        errores.push("Seleccione la fuente de potencia.");
    }

    return {
        ok: errores.length === 0,
        errores
    };
}


