function obtenerValorTexto(id) {
    const elemento = document.getElementById(id);
    if (!elemento) return "";
    return String(elemento.value || "").trim();
}


function obtenerValorNumerico(id) {
    const texto = obtenerValorTexto(id);

    if (texto === "") return NaN;

    const normalizado = texto.replace(",", ".");
    const valor = Number(normalizado);

    return Number.isFinite(valor) ? valor : NaN;
}


function colocarTexto(id, texto) {
    const elemento = document.getElementById(id);
    if (elemento) {
        elemento.textContent = texto;
    }
}


function formatearNumero(valor, decimales = 2) {
    if (!Number.isFinite(Number(valor))) return "-";

    return Number(valor).toLocaleString("es-CO", {
        minimumFractionDigits: decimales,
        maximumFractionDigits: decimales
    });
}


function normalizarNumeroCatalogo(valor) {
    if (valor === null || valor === undefined) return NaN;

    if (typeof valor === "number") return valor;

    const limpio = String(valor)
        .replace(/\s/g, "")
        .replace(",", ".");

    const numero = Number(limpio);

    return Number.isFinite(numero) ? numero : NaN;
}


