function obtenerCandidatosCadena(hpRequeridoPorHilera) {
    const rpmConductor = obtenerValorNumerico("rpmConductorCadena");
    const numeroHileras = Number(obtenerValorTexto("numeroHilerasCadena"));
    const mostrarTodas = debeMostrarTodasOpcionesCadena();
    const sobredimensionMax = obtenerSobredimensionMaximaCadena();

    if (
        !Number.isFinite(hpRequeridoPorHilera) ||
        hpRequeridoPorHilera <= 0 ||
        !Number.isFinite(rpmConductor) ||
        rpmConductor <= 0 ||
        !Number.isFinite(numeroHileras) ||
        numeroHileras <= 0
    ) {
        return [];
    }

    const candidatos = [];

    tablasCapacidadPinonCadena.forEach(tabla => {
        if (!cadenaPermiteHilera(tabla.cadena, numeroHileras)) return;

        tabla.filas.forEach(fila => {
            const capacidadHP = interpolarCapacidadPorRPM(
                tabla.rpm,
                fila.hp,
                rpmConductor
            );

            if (!Number.isFinite(capacidadHP) || capacidadHP <= 0) return;

            const esValida = capacidadHP >= hpRequeridoPorHilera;

            if (!esValida) return;

            const sobredimension = (
                (capacidadHP - hpRequeridoPorHilera) /
                hpRequeridoPorHilera
            ) * 100;

            if (!mostrarTodas && sobredimension > sobredimensionMax) return;

            candidatos.push({
                cadena: String(tabla.cadena),
                ordenCadena: Number(tabla.orden),
                paso: tabla.paso,
                dientes: Number(fila.dientes),
                rpm: rpmConductor,
                capacidadHP,
                hpRequerido: hpRequeridoPorHilera,
                diferenciaHP: capacidadHP - hpRequeridoPorHilera,
                sobredimension,
                numeroHileras
            });
        });
    });

    return candidatos;
}


function ordenarCandidatosCadena(candidatos) {
    const criterio = obtenerValorTexto("criterioSeleccionPinon");

    // CORRECCIÓN IMPORTANTE:
    // Antes estaba: const copia = [.candidatos];
    // Eso rompe todo el JavaScript.
    const copia = [...candidatos];

    switch (criterio) {
        case "pequenoMenosDientes":
            copia.sort((a, b) =>
                a.ordenCadena - b.ordenCadena ||
                a.dientes - b.dientes ||
                a.diferenciaHP - b.diferenciaHP
            );
            break;

        case "pequenoMasDientes":
            copia.sort((a, b) =>
                a.ordenCadena - b.ordenCadena ||
                b.dientes - a.dientes ||
                a.diferenciaHP - b.diferenciaHP
            );
            break;

        case "grandeMenosDientes":
            copia.sort((a, b) =>
                b.ordenCadena - a.ordenCadena ||
                a.dientes - b.dientes ||
                a.diferenciaHP - b.diferenciaHP
            );
            break;

        case "grandeMasDientes":
            copia.sort((a, b) =>
                b.ordenCadena - a.ordenCadena ||
                b.dientes - a.dientes ||
                a.diferenciaHP - b.diferenciaHP
            );
            break;

        case "normal":
        default:
            copia.sort((a, b) =>
                Math.abs(a.diferenciaHP) - Math.abs(b.diferenciaHP) ||
                a.ordenCadena - b.ordenCadena ||
                a.dientes - b.dientes
            );
            break;
    }

    return copia;
}
