// PARCHE PARA INTEGRAR PASO 8 CON EL CÁLCULO DEL PIÑÓN CONDUCIDO
// ======================================================

function calcularPinonConducidoCadena(candidatoConductor) {
    if (!candidatoConductor) {
        limpiarResultadoPinonConducidoCadena();
        limpiarResultadoLongitudCadena();
        return;
    }

    const relacionVelocidad = calcularRelacionVelocidadCadena();

    if (!Number.isFinite(relacionVelocidad) || relacionVelocidad <= 0) {
        colocarTexto("dientesTeoricosConducidoCadena", "Rv inválida");

        colocarTexto(
            "detalleDientesConducidoCadena",
            "Revise las RPM del conductor y del conducido."
        );

        colocarTexto("dientesComercialesConducidoCadena", "-");
        colocarTexto("referenciaDimensionPinonConducidoCadena", "No disponible");

        const contenedor = document.getElementById("tablaDimensionesPinonConducidoCadena");

        if (contenedor) {
            contenedor.innerHTML = `
                <div class="aviso-dimension-no-disponible">
                    No fue posible buscar dimensiones del piñón conducido porque la relación de velocidad no es válida.
                </div>
            `;
        }

        limpiarResultadoDistanciaCentrosCadena(
            "No fue posible calcular la distancia mínima porque la relación de velocidad no es válida."
        );

        limpiarResultadoLongitudCadena(
            "No fue posible calcular la longitud de cadena porque la relación de velocidad no es válida."
        );

        return;
    }

    const dientesConductor = Number(candidatoConductor.dientes);

    if (!Number.isFinite(dientesConductor) || dientesConductor <= 0) {
        limpiarResultadoPinonConducidoCadena();
        limpiarResultadoLongitudCadena();
        return;
    }

    const dientesTeoricos = dientesConductor * relacionVelocidad;
    const dientesComerciales = Math.round(dientesTeoricos);

    colocarTexto(
        "dientesTeoricosConducidoCadena",
        `${formatearNumero(dientesTeoricos, 2)} dientes`
    );

    colocarTexto(
        "detalleDientesConducidoCadena",
        `Z conducido = ${dientesConductor} × ${formatearNumero(relacionVelocidad, 3)} = ${formatearNumero(dientesTeoricos, 3)} dientes`
    );

    colocarTexto(
        "dientesComercialesConducidoCadena",
        `${dientesComerciales} dientes`
    );

    colocarTexto(
        "detalleRedondeoConducidoCadena",
        `${formatearNumero(dientesTeoricos, 3)} ≈ ${dientesComerciales} dientes comerciales`
    );

    const resultadoDimensionConductor = buscarDimensionesPinonCadena(
        candidatoConductor.cadena,
        candidatoConductor.dientes,
        candidatoConductor.numeroHileras
    );

    const resultadoDimensionConducido = buscarDimensionesPinonCadena(
        candidatoConductor.cadena,
        dientesComerciales,
        candidatoConductor.numeroHileras
    );

    mostrarDimensionesPinonConducidoCadena(resultadoDimensionConducido);

    calcularDistanciaCentrosMinimaCadena(
        resultadoDimensionConductor,
        resultadoDimensionConducido
    );

    calcularLongitudCadenaYEslabones(
        resultadoDimensionConductor,
        resultadoDimensionConducido,
        candidatoConductor.cadena
    );
}


