function calcularTodosLosPasosCadena() {
    actualizarFactorSeguridadCadena();

    const relacionVelocidad = calcularRelacionVelocidadCadena();
    const potenciaDiseno = calcularPotenciaDisenoCadena();
    const hpPorHilera = calcularHpPorHileraCadena(potenciaDiseno);

    const validacion = validarDatosMinimosCadena();

    if (!validacion.ok) {
        candidatosCadenaActuales = [];
        candidatoCadenaSeleccionado = null;

        limpiarResultadoSeleccionPinonCadena();

        limpiarTablaCandidatosCadena(
            validacion.errores.join("<br>")
        );

        return;
    }

    if (
        !Number.isFinite(relacionVelocidad) ||
        relacionVelocidad <= 0 ||
        !Number.isFinite(potenciaDiseno) ||
        potenciaDiseno <= 0 ||
        !Number.isFinite(hpPorHilera) ||
        hpPorHilera <= 0
    ) {
        candidatosCadenaActuales = [];
        candidatoCadenaSeleccionado = null;

        limpiarResultadoSeleccionPinonCadena();

        limpiarTablaCandidatosCadena(
            "No fue posible calcular opciones. Revise los datos ingresados."
        );

        return;
    }

    const candidatos = obtenerCandidatosCadena(hpPorHilera);
    const candidatosOrdenados = ordenarCandidatosCadena(candidatos);

    candidatosCadenaActuales = candidatosOrdenados;
    candidatoCadenaSeleccionado = null;

    limpiarResultadoSeleccionPinonCadena();
    renderizarTablaCandidatosCadena(candidatosCadenaActuales);
}


