function calcularLongitudCorrea(dpm, dpc, distanciaCentros) {
    if (!dpm || !dpc || !distanciaCentros) return null;

    let D = Math.max(dpm, dpc);
    let d = Math.min(dpm, dpc);
    let C = distanciaCentros;

    return (2 * C) + (1.57 * (D + d)) + (Math.pow(D - d, 2) / (4 * C));
}

