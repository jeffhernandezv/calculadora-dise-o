function calcularBCorregido(longitudComercial, dpm, dpc) {
    if (!longitudComercial || !dpm || !dpc) return null;

    let D = Math.max(dpm, dpc);
    let d = Math.min(dpm, dpc);

    return (4 * longitudComercial) - (6.28 * (D + d));
}

function calcularDistanciaCentrosCorregida(bc, dpm, dpc) {
    if (!bc || !dpm || !dpc) return null;

    let D = Math.max(dpm, dpc);
    let d = Math.min(dpm, dpc);

    let discriminante = Math.pow(bc, 2) - (32 * Math.pow(D - d, 2));

    if (discriminante < 0) return null;

    return (bc + Math.sqrt(discriminante)) / 16;
}

