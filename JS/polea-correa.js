function calcularPasosParciales() {
    const potenciaInput = document.getElementById('potenciaPrincipal');
    const usarNDInput = document.getElementById('usarND');
    const valorNDInput = document.getElementById('valorND');
    const resPotNDInput = document.getElementById('resPotND');
    const fsManualInput = document.getElementById('fsManual');
    const resPotFSInput = document.getElementById('resPotFS');

    if (!potenciaInput || !usarNDInput || !valorNDInput || !resPotNDInput || !fsManualInput || !resPotFSInput) return;

    const p = parseFloat(potenciaInput.value);
    const fs = parseFloat(fsManualInput.value);

    if (!Number.isFinite(p) || p <= 0) {
        resPotNDInput.value = usarNDInput.checked ? "" : "N/A";
        resPotFSInput.value = "";
        return;
    }

    if (!Number.isFinite(fs) || fs <= 0) {
        resPotFSInput.value = "";
        return;
    }

    if (usarNDInput.checked) {
        const nd = parseFloat(valorNDInput.value);

        if (!Number.isFinite(nd) || nd <= 0) {
            resPotNDInput.value = "";
            resPotFSInput.value = "";
            return;
        }

        const pConNd = p * nd;
        const motorBaseCalculos = buscarMotorComercial(pConNd);
        const pdFinal = motorBaseCalculos * fs;

        resPotNDInput.value = `${pConNd.toFixed(3)} -> Motor base cálculos: ${motorBaseCalculos} HP`;
        resPotFSInput.value = `${pdFinal.toFixed(3)} HP`;
    } else {
        const potenciaBaseCalculos = p;
        const pdFinal = p * fs;

        resPotNDInput.value = "N/A";
        resPotFSInput.value = `${pdFinal.toFixed(3)} HP | Base cálculos: ${potenciaBaseCalculos.toFixed(3)} HP`;
    }
}
