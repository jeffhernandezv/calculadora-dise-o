function aplicarVersionX(seccionBase, tecnologia) {
    if (!seccionBase || !tecnologia) return seccionBase;

    if (tecnologia === "convencionalX") {
        if (seccionBase === "A") return "AX";
        if (seccionBase === "B") return "BX";
        if (seccionBase === "C") return "CX";

        // La familia D se mantiene como D.
        return "D";
    }

    if (tecnologia === "altaCapacidadX") {
        if (seccionBase === "3V") return "3VX";
        if (seccionBase === "5V") return "5VX";

        // La familia 8V se mantiene como 8V.
        return "8V";
    }

    return seccionBase;
}

function obtenerNombreTecnologia(tecnologia, seccion) {
    if (tecnologia === "convencional") {
        return "Convencional Estándar";
    }

    if (tecnologia === "convencionalX") {
        if (seccion === "D") {
            return "Convencional X seleccionada | Se usa D estándar";
        }

        return "Convencional Dentada / X";
    }

    if (tecnologia === "altaCapacidad") {
        return "Alta Capacidad Estándar";
    }

    if (tecnologia === "altaCapacidadX") {
        if (seccion === "8V") {
            return "Alta Capacidad X seleccionada | Se usa 8V estándar";
        }

        return "Alta Capacidad Dentada / X";
    }

    return "No definida";
}

function obtenerSeccionBanda(pd, rpm, tecnologia) {
    let seccionBase = null;

    if (tecnologia === "convencional" || tecnologia === "convencionalX") {
        if (rpm >= 1750) {
            if (pd <= 18.0) seccionBase = "A";
            else if (pd <= 70.0) seccionBase = "B";
            else if (pd <= 180.0) seccionBase = "C";
            else seccionBase = "D";
        } else if (rpm >= 1160) {
            if (pd <= 12.0) seccionBase = "A";
            else if (pd <= 45.0) seccionBase = "B";
            else if (pd <= 150.0) seccionBase = "C";
            else seccionBase = "D";
        } else if (rpm >= 870) {
            if (pd <= 10.0) seccionBase = "A";
            else if (pd <= 35.0) seccionBase = "B";
            else if (pd <= 130.0) seccionBase = "C";
            else seccionBase = "D";
        } else {
            if (pd <= 7.0) seccionBase = "A";
            else if (pd <= 25.0) seccionBase = "B";
            else if (pd <= 100.0) seccionBase = "C";
            else seccionBase = "D";
        }
    }

    if (tecnologia === "altaCapacidad" || tecnologia === "altaCapacidadX") {
        if (rpm >= 1750) {
            if (pd <= 60.0) seccionBase = "3V";
            else if (pd <= 350.0) seccionBase = "5V";
            else seccionBase = "8V";
        } else if (rpm >= 1160) {
            if (pd <= 45.0) seccionBase = "3V";
            else if (pd <= 300.0) seccionBase = "5V";
            else seccionBase = "8V";
        } else {
            if (pd <= 30.0) seccionBase = "3V";
            else if (pd <= 200.0) seccionBase = "5V";
            else seccionBase = "8V";
        }
    }

    return aplicarVersionX(seccionBase, tecnologia);
}


