    let velocidadLinealConducida = calcularVelocidadLinealCorrea(rpmSalida, dpc);

    let textoCorreaComercial = correaComercial
        ? `${correaComercial.numero} = ${correaComercial.largo.toFixed(1)}" | F.C Longitud = ${correaComercial.fc.toFixed(2)}`
        : "No disponible en tabla";

